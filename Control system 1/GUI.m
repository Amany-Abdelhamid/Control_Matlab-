% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
a1=str2double(get(handles.edit1,'String'));
a2=str2double(get(handles.edit3,'String'));
a3=str2double(get(handles.edit4,'String'));
a4=str2double(get(handles.edit5,'String'));
   Ts=0.1;
    Kp =         a2;           % Proportional
    Ki =         a3;             % Integral
    Kd =        a4;             % Derivative
    
    u_buffer  =  0;
    h_buffer  =  0;             % system output buffeer
    t_buffer  =  0;             % time buffer --> x axis
   
    ui_old =     0;             % ui(n-1) previous integrator output
    e_old  =     0;             % e(n-1) previous error value 
    u_k1 =0;u_k2=0;u=0;h_k1=0;h_k2=0;h_k3=0;
   
    set_point = a1;
    
    for t=0:Ts:50
        
        h = 0.000251*u +0.0007569*u_k1 +0.000138*u_k2 +2.186498*h_k1 -1.48769*h_k2 +0.30119*h_k3;  % Get output value from the model
        e = set_point-h;               % Get the error
       
        ui_new = ui_old + Ts*e;            % Integral part
        ud     = (e-e_old) / Ts;           % Derivative part
        u      = Kp*e + Ki*ui_new + Kd*ud; % Output of the controller
       
        u_buffer  = [u_buffer u];
        h_buffer  = [h_buffer h];
        t_buffer  = [t_buffer t];
      
        e_old=e;     ui_old=ui_new;
        h_k3=h_k2;  h_k2=h_k1;   h_k1=h;
        u_k2=u_k1;   u_k1=u;
    end
         axes(handles.axes1);      plot(t_buffer,h_buffer);
         axes(handles.axes2);      plot(t_buffer,u_buffer);
